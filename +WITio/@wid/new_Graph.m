function obj = new_Graph(O_wit),
    if nargin == 0 || isempty(O_wit), O_wit = WITio.wid.new(); end % Create O_wit
    Version = WITio.wip.get_Root_Version(O_wit);
    
    Tag_DataClassName = WITio.wit('DataClassName 0', 'TDGraph');
    Tag_Data = WITio.wit('Data 0');
    
    Tag_TData = WITio.wip.new_TData(Version, sprintf('New %s', Tag_DataClassName.Data));
    if isempty(Version) || Version == 7,
        Tag_TDGraph = WITio.wit('TDGraph', [ ...
            WITio.wit('Version', int32(1)) ...
            WITio.wit('SizeX', int32(1)) WITio.wit('SizeY', int32(0)) WITio.wit('SizeGraph', int32(0)) ...
            WITio.wit('SpaceTransformationID', int32(0)) ...
            WITio.wit('SecondaryTransformationID', int32(0)) ...
            WITio.wit('XTransformationID', int32(0)) ...
            WITio.wit('XInterpretationID', int32(0)) ...
            WITio.wit('ZInterpretationID', int32(0)) ...
            WITio.wit('DataFieldInverted', true) ...
            WITio.wit('GraphData', [WITio.wit('Dimension', int32(2)) WITio.wit('DataType', int32(10)) WITio.wit('Ranges', int32([1 0])) WITio.wit('Data', uint8.empty)]) ...
            WITio.wit('LineChanged', logical.empty) WITio.wit('LineValid', logical.empty) ...
            ]);
        Tag_Data.Data = [Tag_TData Tag_TDGraph];
    elseif Version == 6,
        Tag_TDGraph = WITio.wit('TDGraph', [ ...
            WITio.wit('Version', int32(1)) ...
            WITio.wit('SizeX', int32(1)) WITio.wit('SizeY', int32(0)) WITio.wit('SizeGraph', int32(0)) ...
            WITio.wit('SpaceTransformationID', int32(0)) ...
            WITio.wit('XTransformationID', int32(0)) ...
            WITio.wit('XInterpretationID', int32(0)) ...
            WITio.wit('ZInterpretationID', int32(0)) ...
            WITio.wit('DataFieldInverted', true) ...
            WITio.wit('GraphData', [WITio.wit('Dimension', int32(2)) WITio.wit('DataType', int32(10)) WITio.wit('Ranges', int32([1 0])) WITio.wit('Data', uint8.empty)]) ...
            WITio.wit('LineChanged', logical.empty) WITio.wit('LineValid', logical.empty) ...
            ]);
        Tag_Data.Data = [Tag_TData Tag_TDGraph];
    elseif Version >= 0 && Version <= 5, % Legacy versions
        Tag_TDGraph = WITio.wit('TDGraph', [ ...
            WITio.wit('Version', int32(0)) ...
            WITio.wit('SizeX', int32(1)) WITio.wit('SizeY', int32(0)) WITio.wit('SizeGraph', int32(0)) ...
            WITio.wit('SpaceTransformationID', int32(0)) ...
            WITio.wit('XTransformationID', int32(0)) ...
            WITio.wit('XInterpretationID', int32(0)) ...
            WITio.wit('ZInterpretationID', int32(0)) ...
            WITio.wit('GraphData', [WITio.wit('Dimension', int32(2)) WITio.wit('DataType', int32(10)) WITio.wit('Ranges', int32([1 0])) WITio.wit('Data', uint8.empty)]) ...
            WITio.wit('LineChanged', logical.empty) WITio.wit('LineValid', logical.empty) ...
            ]);
        Tag_Data.Data = [Tag_TData Tag_TDGraph];
    else, error('Unimplemented Version (%d)!', Version); end
    
    % Append these to the given (or created) O_wit
    [~, Pair] = WITio.wip.append(O_wit, [Tag_DataClassName Tag_Data]);
    
    % Create new wid
    obj = WITio.wid(Pair);
end
