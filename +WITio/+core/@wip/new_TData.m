function O_wit = new_TData(Version, Caption),
    if nargin < 2 || isempty(Version) || Version == 7,
        Tag_TData = WITio.core.wit('TData', [ ...
            WITio.core.wit('Version', int32(0)) ...
            WITio.core.wit('ID', int32(1)) ... % To be updated later in WITio.core.wip.append
            WITio.core.wit('ImageIndex', int32(0)) ...
            WITio.core.wit('Caption', Caption) ...
            WITio.core.wit('MetaData', WITio.core.wit.empty) ...
            WITio.core.wit('HistoryList', [WITio.core.wit('Number Of History Entries', int32(0)) WITio.core.wit('Dates', uint32.empty) WITio.core.wit('Histories', '') WITio.core.wit('Types', int32.empty)]) ...
            ]);
    elseif Version >= 0 && Version <= 5 || Version == 6, % Legacy versions OR v6
        Tag_TData = WITio.core.wit('TData', [ ...
            WITio.core.wit('Version', int32(0)) ...
            WITio.core.wit('ID', int32(1)) ... % To be updated later in WITio.core.wip.append
            WITio.core.wit('ImageIndex', int32(0)) ...
            WITio.core.wit('Caption', Caption) ...
            WITio.core.wit('HistoryList', [WITio.core.wit('Number Of History Entries', int32(0)) WITio.core.wit('Dates', uint32.empty) WITio.core.wit('Histories', '') WITio.core.wit('Types', int32.empty)]) ...
            ]);
    else, error('Unimplemented Version (%d)!', Version); end
    O_wit = Tag_TData;
end
